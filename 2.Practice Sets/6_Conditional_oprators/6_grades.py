# programme for calculate users grade depends on the marks.
users_marks = int(input("Enter your marks : "))

if(users_marks>=90 and users_marks<100):
    print("Grade for your marks is : EX")
elif(users_marks>=80 and users_marks<90):
    print("Grade for your marks is : A")
elif(users_marks>=70 and users_marks<80):
    print("Grade for your marks is : B")
elif(users_marks>=60 and users_marks<70):
    print("Grade for your marks is : C")
elif(users_marks>=50 and users_marks<60):
    print("Grade for your marks is : D")
else:
    print("Grade for your marks is : F")




    

